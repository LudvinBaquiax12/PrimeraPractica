    private void btnIngresoApuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresoApuestaActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();

        //FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.txt", "txt");
        //fileChooser.setFileFilter(filtro);
        String datos = "";
        int seleccion = fileChooser.showOpenDialog(this);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            String ruta = fileChooser.getSelectedFile().getAbsolutePath();
            try (FileReader entrada = new FileReader(ruta)) {
                BufferedReader miBuffer = new BufferedReader(entrada);
                String linea = "";

                while (linea != null) {
                    linea = miBuffer.readLine();
                    if (linea != null) {
                        datos = datos + linea + "\n";
                    }
                }
            } catch (FileNotFoundException ex) {

            } catch (IOException ex) {

            }
        } else if (seleccion == JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ning√∫n fichero", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else if (rootPaneCheckingEnabled) {
            JOptionPane.showMessageDialog(this, "Error en la busqueda del archivo", "Error", JOptionPane.ERROR_MESSAGE);
        }
        if (!datos.isEmpty()) {
            IngresoDeDatos ingresoDeDatos = new IngresoDeDatos(this, datos);
        }
    }//GEN-LAST:event_btnIngresoApuestaActionPerformed

